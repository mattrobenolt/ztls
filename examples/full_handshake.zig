//! TLS 1.3 client handshake driven through the public ClientHandshake API.
//!
//! This runs the real driver — init → start → processRecord loop — against the
//! RFC 8448 §3 server records (ServerHello, ChangeCipherSpec, and the encrypted
//! EncryptedExtensions/Certificate/CertificateVerify/Finished flight). Because
//! the server's records are AEAD-protected under keys derived from §3's
//! transcript, the client must feed the exact §3 ClientHello bytes — so this
//! example exercises the handshake driver, not client_hello.encode.
//!
//! After the handshake completes, rx/tx carry application traffic keys; the
//! finale round-trips a message through them to show they are live.
